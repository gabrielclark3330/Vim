#!/bin/bash

### Installing VimPlug
sh -c 'curl -fLo "${XDG_DATA_HOME:-$HOME/.local/share}"/nvim/site/autoload/plug.vim --create-dirs \
       https://raw.githubusercontent.com/junegunn/vim-plug/master/plug.vim'

### Installing ripgrep
sudo apt install ripgrep

### Installing rd
sudo apt install fd-find

